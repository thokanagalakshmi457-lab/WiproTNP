package Arrays;

public class MiddleWay {

    public static void main(String[] args) {

        int[] a = {1, 2, 3};
        int[] b = {4, 5, 6};

        int[] result = {a[1], b[1]};

        System.out.print("[");
        System.out.print(result[0] + ", " + result[1]);
        System.out.print("]");
    }
}